
public class IfExample3 {
	public static void main(String[] args) {
		/*detect the character is  vowels or not */
		
		char ch='a';
		
		if(ch=='a') {
			System.out.println("it is vowel");
		}else if(ch=='e') {
			System.out.println("It is vowel");
		}else if(ch=='i') {
			System.out.println("It is vowel");
		}else if(ch=='o') {
			System.out.println("It is vowel");
		}else if(ch=='u') {
			System.out.println("It is vowel");
		}else {
			System.out.println("It is not a vowel");
		}

		/*Assingment - A E I O U*/
		
		
	}
}
