package com.myapp.basic;

public class OperatorsAssignment {
	public static void main(String[] args) {
		/*
		  	=	
		  	+=	[C += A] 
	      	-=	
	      	*=	
	      	/=	 
	      	%=	
	 */
		
		int c=100;
		int a=10;
		c+=a;
		System.out.println("Result-1 "+c);
		
		c*=a;
		System.out.println("Result-2 "+c);
	}
}
